// Map codecs to available encoders
export function getEncoderMap(ffmpegCapabilities: string) {
	return {
		opus: 'libopus',
		vorbis: 'libvorbis',
		av1: 'libsvtav1',
		aac: ffmpegCapabilities.includes('libfdk-aac') ? 'libfdk_aac' : 'aac',
		mp3: 'libmp3lame',
		h264: 'libx264',
		h265: 'libx265',
		hevc: 'libx265',
	};
}
